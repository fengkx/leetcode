class ListNode {
    val: number
    next: ListNode | null
    constructor(val?: number, next?: ListNode | null) {
        this.val = (val===undefined ? 0 : val)
        this.next = (next===undefined ? null : next)
    }
}

function reversePrint(head: ListNode | null): number[] {
    const stack: number[] = [];
    let cur = head;
    while(cur) {
        stack.push(cur.val)
        cur = cur.next
    }
    return stack.reverse()
};
